#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*s1;
	int		i;

	i = 0;
	s1 = (char *)s;
	while (s1[i] != '\0' && s1[i] != (char)c)
		i++;
	if (s1[i] == (char)c)
		return (&(s1[i]));
	return (0);
}
