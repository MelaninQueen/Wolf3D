#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	i;
	char	*b1;

	i = 0;
	b1 = (char *)b;
	while (i < len)
	{
		b1[i] = (char)c;
		i++;
	}
	return (b1);
}
